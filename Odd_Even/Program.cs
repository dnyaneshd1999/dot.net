using System;

namespace Odd_Even
{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 2;
            if (i % 2 == 0)
            {
                Console.WriteLine("NO is Even");
            }
            else
            {
                Console.WriteLine("NO is Odd");
            }
            Console.WriteLine("Hello World!");
        }
    }
}
